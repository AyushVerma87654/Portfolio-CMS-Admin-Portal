import instance from "./axios";

export const uploadImage = async (file: File) => {
  const formData = new FormData();
  formData.append("file", file);
  return instance
    .post("/upload-image", formData, {
      headers: {
        "Content-Type": "multipart/form-data",
      },
    })
    .then((res) => res.data);
};
